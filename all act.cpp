#include<iostream>
using namespace std;
int main()
{
	int a,b,sum,diff,prod,div;
	cout<<" Enter two numbers:";
	cin>>a>>b;
	sum=a+b;
	diff=a-b;
	prod=a*b;
	div=a/b;
	cout<<" Sum of numbers:"<<sum<<endl;
	cout<<" Product of numbers:"<<prod<<endl;
	cout<<" Difference of numbers:"<<diff<<endl;
	cout<<" Quotient of numbers:"<<div<<endl;
}